unsigned int get_ceq_stage(unsigned char resol, unsigned int acc_gain)
{
	unsigned char c_eq = 0;

	if(resol == NVP6124_VI_1080P_2530)
	{
		if	   (acc_gain >= 0x000 && acc_gain < 0x052 )   c_eq = 1;
		else if(acc_gain >= 0x052 && acc_gain < 0x089 )   c_eq = 2;
		else if(acc_gain >= 0x089 && acc_gain < 0x113 )   c_eq = 3;
		else if(acc_gain >= 0x113 && acc_gain < 0x25F )   c_eq = 4;
		else if(acc_gain >= 0x25F && acc_gain < 0x700 )   c_eq = 5;
		else if(acc_gain >= 0x700 && acc_gain < 0x7FF )   c_eq = 6;
		else											  c_eq = 7;  
	}
	else if(resol == NVP6124_VI_720P_2530)
	{
		if	   (acc_gain >= 0x000 && acc_gain < 0x055 )  c_eq = 1;
		else if(acc_gain >= 0x055 && acc_gain < 0x082 )  c_eq = 2;
		else if(acc_gain >= 0x082 && acc_gain < 0x0D8 )  c_eq = 3;
		else if(acc_gain >= 0x0D8 && acc_gain < 0x18F )  c_eq = 4;
		else if(acc_gain >= 0x18F && acc_gain < 0x700 )  c_eq = 5;
		else if(acc_gain >= 0x700 && acc_gain < 0x7FF )  c_eq = 6;
		else											 c_eq = 7;  
	}

	return c_eq;
}

unsigned int get_yeq_stage(unsigned char resol, unsigned int y_minus_slp)
{
	unsigned char y_eq = 0;

	if(resol == NVP6124_VI_1080P_2530)
	{
		if     (y_minus_slp == 0x000)						    y_eq = 0;
		else if(y_minus_slp >  0x000 && y_minus_slp < 0x0E7)    y_eq = 1;
		else if(y_minus_slp >= 0x0E7 && y_minus_slp < 0x11A)    y_eq = 2;
		else if(y_minus_slp >= 0x11A && y_minus_slp < 0x151)    y_eq = 3;
		else if(y_minus_slp >= 0x151 && y_minus_slp < 0x181)    y_eq = 4;
		else if(y_minus_slp >= 0x181 && y_minus_slp < 0x200)    y_eq = 5;
		else													y_eq = 6;
	}
	else if(resol == NVP6124_VI_720P_2530)
	{
		if     (y_minus_slp == 0x000)                           y_eq = 0;
		else if(y_minus_slp >  0x000 && y_minus_slp < 0x104)    y_eq = 1;
		else if(y_minus_slp >= 0x104 && y_minus_slp < 0x125)    y_eq = 2;
		else if(y_minus_slp >= 0x125 && y_minus_slp < 0x14C)    y_eq = 3;
		else if(y_minus_slp >= 0x14C && y_minus_slp < 0x16F)    y_eq = 4;
		else if(y_minus_slp >= 0x16F && y_minus_slp < 0x185)    y_eq = 5;
		else													y_eq = 6;
	}

	return y_eq;

}

unsigned int is_bypass_mode(unsigned int c_stage, unsigned int c_status, unsigned int agc_val, unsigned int bw_on, unsigned int y_ref2_status)
{
	printf("is_bypass_mode : c_stage %d c_status %x agc_val %x bw_on %d y_ref2_status %x\n",c_stage,c_status,agc_val,bw_on,y_ref2_status);
	if(bw_on)
	{
		if(((agc_val < 0x20)  && (y_ref2_status >= 0x176))			||
		   ((agc_val >= 0x20) && (y_ref2_status >= 0x1B0)))          
				return 1;
	}
	else
	{
		if(((c_stage > 0 && c_stage < 3) && y_ref2_status >= 0x160) ||
		   ((c_stage == 3)               && y_ref2_status >= 0x168) ||
		   ((c_stage == 4)               && y_ref2_status >= 0x195) )
				return 1;
	}

	return 0;
}

volatile unsigned char c_same_cnt[NVP6124_CHNL_NUM_MAX],y_same_cnt[NVP6124_CHNL_NUM_MAX];
volatile unsigned char check_c_stage[NVP6124_CHNL_NUM_MAX], check_c_stage_back[NVP6124_CHNL_NUM_MAX];
volatile unsigned char check_y_stage[NVP6124_CHNL_NUM_MAX], check_y_stage_back[NVP6124_CHNL_NUM_MAX];
volatile unsigned char vidmode_back[NVP6124_CHNL_NUM_MAX];
volatile unsigned char video_on[NVP6124_CHNL_NUM_MAX];
void nvp6124_set_equalizer(int addr, int chnl, int ch_vloss, int ch_mode_status)
{
	unsigned char vidmode[NVP6124_CHNL_NUM_MAX];
	unsigned char agc_lock;
	unsigned int  agc_val[NVP6124_CHNL_NUM_MAX];
	unsigned int  acc_gain_sts[NVP6124_CHNL_NUM_MAX];
	unsigned int  y_ref_status[NVP6124_CHNL_NUM_MAX];
	unsigned int  y_ref2_status[NVP6124_CHNL_NUM_MAX];
	unsigned char bw_on[NVP6124_CHNL_NUM_MAX];

	int ch = chnl;

	I2C_InLock();

	HDDVR_i2c_WriteByte(addr, 0xFF, 0x09);
	HDDVR_i2c_WriteByte(addr, 0x61, ch%4);
	HDDVR_i2c_WriteByte(addr, 0xFF, 0x00);
	agc_val[ch] = HDDVR_i2c_ReadByte(addr, 0xF7);
	HDDVR_i2c_WriteByte(addr, 0xFF, 0x00);
	vidmode[ch] = HDDVR_i2c_ReadByte(addr, 0xD0+ch%4);

	agc_lock = HDDVR_i2c_ReadByte(addr, 0xEC);
	bw_on[ch] = (HDDVR_i2c_ReadByte(addr, 0xF3)>>(ch%4))&0x01;

	I2C_UnLock();

	I2C_InLock();

	HDDVR_i2c_WriteByte(addr, 0xFF, 0x05 + ch%4);
	acc_gain_sts[ch] = HDDVR_i2c_ReadByte(addr, 0xE2)&0x07;
	acc_gain_sts[ch] <<= 8;
	acc_gain_sts[ch] |= HDDVR_i2c_ReadByte(addr, 0xE3);
	
	y_ref_status[ch] = HDDVR_i2c_ReadByte(addr, 0xEA)&0x07;
	y_ref_status[ch] <<= 8;
	y_ref_status[ch] |= HDDVR_i2c_ReadByte(addr, 0xEB);

	y_ref2_status[ch] = HDDVR_i2c_ReadByte(addr, 0xE8)&0x07;
	y_ref2_status[ch] <<= 8;
	y_ref2_status[ch] |= HDDVR_i2c_ReadByte(addr, 0xE9);

	I2C_UnLock();

	if(vidmode[ch] >= 4) {
		if((!ch_vloss) && (((agc_lock>>(ch%4))&0x01) == 0x01))
			video_on[ch] = 1;
		else
			video_on[ch] = 0;
	}
	else {
		video_on[ch] = 0;
	}

	if((ch_mode_status == NVP6124_VI_1080P_2530)
		|| (ch_mode_status == NVP6124_VI_720P_2530)) {

		I2C_InLock();

		if(video_on[ch]) {
			check_c_stage[ch] = get_ceq_stage(ch_mode_status, acc_gain_sts[ch]);
			check_y_stage[ch] = get_yeq_stage(ch_mode_status, y_ref_status[ch]);

			if(ch_mode_status == NVP6124_VI_720P_2530) {
				if(is_bypass_mode(check_c_stage[ch], acc_gain_sts[ch],agc_val[ch], bw_on[ch],y_ref2_status[ch]))
					check_y_stage[ch] = 7;

				if(((check_c_stage[ch] == 5)   && y_ref2_status[ch] >= 0x1A0) ||
					(acc_gain_sts[ch] == 0x7FF && y_ref2_status[ch] >= 0x1B0))
					check_y_stage[ch] = 8;

				if(y_same_cnt[ch] != 0xFF) {
					if(check_c_stage[ch] == check_c_stage_back[ch]) {
						c_same_cnt[ch]++;
						if(c_same_cnt[ch] > 200 ) c_same_cnt[ch] = 5;
					}
					else {
						c_same_cnt[ch] = 0;
					}

					if(check_y_stage[ch] == check_y_stage_back[ch]) {
						y_same_cnt[ch]++;
						if(y_same_cnt[ch] > 200 ) y_same_cnt[ch] = 5;
					}
					else {
						y_same_cnt[ch] = 0;
					}

					if(c_same_cnt[ch] != y_same_cnt[ch]) {
						c_same_cnt[ch] = 0;
						y_same_cnt[ch] = 0;
					}
				}

				if((c_same_cnt[ch]==4) && (y_same_cnt[ch]==4))
					stage_update[ch] = 1;
				else
					stage_update[ch] = 0;
			}
			else {
				if(y_same_cnt[ch] != 0xFF) {
					if(check_y_stage[ch] == check_y_stage_back[ch]) {
						y_same_cnt[ch]++;
						if(y_same_cnt[ch] > 200 ) y_same_cnt[ch] = 5;
					}
					else {
						y_same_cnt[ch]=0;
					}
				}

				if((y_same_cnt[ch]==4))
					stage_update[ch] = 1;
				else
					stage_update[ch] = 0;
			}
		}
		else {
			if(vidmode_back[ch] >= 0x04) 
				stage_update[ch] = 1;
			else
				stage_update[ch] = 0;

			c_same_cnt[ch]=0;
			y_same_cnt[ch]=0;
			check_c_stage[ch]=0;
			check_c_stage_back[ch]=0xFF;
			check_y_stage[ch]=0;
			check_y_stage_back[ch]=0xFF;
			eq_stage[ch] = 0;
		}

		I2C_UnLock();

		I2C_InLock();

		if(stage_update[ch]) {
			if(video_on[ch]) y_same_cnt[ch] = 0xFF;
			eq_stage[ch] = check_y_stage[ch];

			if(ch_mode_status == NVP6124_VI_720P_2530) {
				HDDVR_i2c_WriteByte(addr, 0xFF,0x05+ch%4);
				HDDVR_i2c_WriteByte(addr, 0x58, ANALOG_EQ_720P[eq_stage[ch]]);
				HDDVR_i2c_WriteByte(addr, 0xFF,((ch%4)<2)?0x0A:0x0B);         
				HDDVR_i2c_WriteByte(addr, (ch%2==0)?0x3B:0xBB, DIGITAL_EQ_720P[eq_stage[ch]]);
			}
			else {
				HDDVR_i2c_WriteByte(addr, 0xFF,0x05+ch%4);
				HDDVR_i2c_WriteByte(addr, 0x58, ANALOG_EQ_1080P[eq_stage[ch]]);
				HDDVR_i2c_WriteByte(addr, 0xFF,((ch%4)<2)?0x0A:0x0B);         
				HDDVR_i2c_WriteByte(addr, (ch%2==0)?0x3B:0xBB, DIGITAL_EQ_1080P[eq_stage[ch]]);
			}
			#ifdef _EQ_ADJ_COLOR_
			nvp6124_brightness_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_contrast_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_saturation_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			#endif
			nvp6124_sharpness_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_peaking_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_ctigain_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_c_filter_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_ugain_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			nvp6124_vgain_eq(addr, ch, ch_mode_status, eq_stage[ch]);
			printf("CH[%d]-Stage update : eq_stage = %d\n",ch,eq_stage[ch]);
		}

		check_c_stage_back[ch] = check_c_stage[ch];
		check_y_stage_back[ch] = check_y_stage[ch];
		vidmode_back[ch] = vidmode[ch];

		I2C_UnLock();
	}
}

